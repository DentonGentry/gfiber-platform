int main(int argc, char** argv)
{
  int sv[2];
  char response[256];
  const char *expected;
  ssize_t len;
  L2Map l2map;

  if (socketpair(AF_UNIX, SOCK_DGRAM, 0, sv)) {
    perror("socketpair");
    exit(1);
  }

  /* --- Normal response --- */
  len = sizeof(asus_pkt_normal);
  if (write(sv[0], asus_pkt_normal, len) != len) {
    fprintf(stderr, "write too short\n");
    exit(1);
  }

  response[0] = '\0';
  if (receive_response(sv[1], &l2map, response, sizeof(response)) != 0) {
    fprintf(stderr, "receive_response could not parse packet\n");
    exit(1);
  }

  expected = "RT-AC68U";
  if (!strstr(response, expected)) {
    printf("Incorrect response %s != %s\n", response, expected);
    exit(1);
  }

  /* --- not a PACKET_TYPE_RESULT --- */
  len = sizeof(asus_pkt_wrong_type);
  if (write(sv[0], asus_pkt_wrong_type, len) != len) {
    fprintf(stderr, "write too short\n");
    exit(1);
  }

  response[0] = '\0';
  if (receive_response(sv[1], &l2map, response, sizeof(response)) == 0) {
    fprintf(stderr, "receive_response should not parse packet\n");
    exit(1);
  }

  if (strlen(response) != 0) {
    printf("Shouldn't return anything for wrong type: %s\n", response);
    exit(1);
  }

  /* --- too short --- */
  len = sizeof(asus_pkt_too_short);
  if (write(sv[0], asus_pkt_too_short, len) != len) {
    fprintf(stderr, "write too short\n");
    exit(1);
  }

  response[0] = '\0';
  if (receive_response(sv[1], &l2map, response, sizeof(response)) == 0) {
    fprintf(stderr, "receive_response should not parse packet\n");
    exit(1);
  }

  if (strlen(response) != 0) {
    printf("Shouldn't return anything for short response: %s\n", response);
    exit(1);
  }

  /* --- Embedded newline --- */
  len = sizeof(asus_pkt_newline);
  if (write(sv[0], asus_pkt_newline, len) != len) {
    fprintf(stderr, "write too short\n");
    exit(1);
  }

  response[0] = '\0';
  if (receive_response(sv[1], &l2map, response, sizeof(response)) != 0) {
    fprintf(stderr, "receive_response could not parse packet\n");
    exit(1);
  }

  expected = "RT-AC.68U";  /* replace the newline with '.' */
  if (!strstr(response, expected)) {
    printf("Incorrect response %s != %s\n", response, expected);
    exit(1);
  }

  exit(0);
}
